export function getServersByISP(ispName: string) {
  return PAKISTAN_SERVERS.filter(s => s.isp.toLowerCase() === ispName.toLowerCase())
}

export function getServersByCity(cityName: string) {
  return PAKISTAN_SERVERS.filter(s => s.city.toLowerCase() === cityName.toLowerCase())
}

export function getAllISPs() {
  const ispMap: Record<string, any> = {}
  PAKISTAN_SERVERS.forEach(s => {
    if (!ispMap[s.isp]) {
      ispMap[s.isp] = { name: s.isp, cities: [], servers: [] }
    }
    if (!ispMap[s.isp].cities.includes(s.city)) {
      ispMap[s.isp].cities.push(s.city)
    }
    ispMap[s.isp].servers.push(s)
  })
  return Object.values(ispMap)
}

export function findServerById(id: string) {
  return PAKISTAN_SERVERS.find(s => s.id === id)
}
